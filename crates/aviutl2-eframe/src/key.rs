pub fn egui_key_to_windows_key(key: eframe::egui::Key) -> Option<u32> {
    use eframe::egui::Key::*;
    match key {
        ArrowDown => Some(0x28),
        ArrowLeft => Some(0x25),
        ArrowRight => Some(0x27),
        ArrowUp => Some(0x26),
        Escape => Some(0x1B),
        Tab => Some(0x09),
        Backspace => Some(0x08),
        Enter => Some(0x0D),
        Space => Some(0x20),
        Insert => Some(0x2D),
        Delete => Some(0x2E),
        Home => Some(0x24),
        End => Some(0x23),
        PageUp => Some(0x21),
        PageDown => Some(0x22),
        Copy | Cut | Paste => None,
        Colon | Semicolon => Some(0xBA),
        Comma => Some(0xBC),
        Backslash | Pipe => Some(0xDC),
        Slash | Questionmark => Some(0xBF),
        Exclamationmark => Some(0x31),
        OpenBracket | OpenCurlyBracket => Some(0xDB),
        CloseBracket | CloseCurlyBracket => Some(0xDD),
        Backtick => Some(0xC0),
        Minus => Some(0xBD),
        Period => Some(0xBE),
        Plus | Equals => Some(0xBB),
        Quote => Some(0xDE),
        Num0 => Some(0x30),
        Num1 => Some(0x31),
        Num2 => Some(0x32),
        Num3 => Some(0x33),
        Num4 => Some(0x34),
        Num5 => Some(0x35),
        Num6 => Some(0x36),
        Num7 => Some(0x37),
        Num8 => Some(0x38),
        Num9 => Some(0x39),
        A => Some(0x41),
        B => Some(0x42),
        C => Some(0x43),
        D => Some(0x44),
        E => Some(0x45),
        F => Some(0x46),
        G => Some(0x47),
        H => Some(0x48),
        I => Some(0x49),
        J => Some(0x4A),
        K => Some(0x4B),
        L => Some(0x4C),
        M => Some(0x4D),
        N => Some(0x4E),
        O => Some(0x4F),
        P => Some(0x50),
        Q => Some(0x51),
        R => Some(0x52),
        S => Some(0x53),
        T => Some(0x54),
        U => Some(0x55),
        V => Some(0x56),
        W => Some(0x57),
        X => Some(0x58),
        Y => Some(0x59),
        Z => Some(0x5A),
        F1 => Some(0x70),
        F2 => Some(0x71),
        F3 => Some(0x72),
        F4 => Some(0x73),
        F5 => Some(0x74),
        F6 => Some(0x75),
        F7 => Some(0x76),
        F8 => Some(0x77),
        F9 => Some(0x78),
        F10 => Some(0x79),
        F11 => Some(0x7A),
        F12 => Some(0x7B),
        F13 => Some(0x7C),
        F14 => Some(0x7D),
        F15 => Some(0x7E),
        F16 => Some(0x7F),
        F17 => Some(0x80),
        F18 => Some(0x81),
        F19 => Some(0x82),
        F20 => Some(0x83),
        F21 => Some(0x84),
        F22 => Some(0x85),
        F23 => Some(0x86),
        F24 => Some(0x87),
        F25 | F26 | F27 | F28 | F29 | F30 | F31 | F32 | F33 | F34 | F35 => None,
        BrowserBack => Some(0xA6),
    }
}
